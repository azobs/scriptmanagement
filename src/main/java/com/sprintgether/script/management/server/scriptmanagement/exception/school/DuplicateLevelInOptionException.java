package com.sprintgether.script.management.server.scriptmanagement.exception.school;

public class DuplicateLevelInOptionException extends Exception {
    public DuplicateLevelInOptionException() {
    }

    public DuplicateLevelInOptionException(String message) {
        super(message);
    }

    public DuplicateLevelInOptionException(String message, Throwable cause) {
        super(message, cause);
    }

    public DuplicateLevelInOptionException(Throwable cause) {
        super(cause);
    }

    public DuplicateLevelInOptionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
